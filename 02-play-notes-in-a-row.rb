# If we want to play successive notes we need to put a sleep inbetween notes
# To do this we use the command : sleep
# The sleep command makes the computer wait
# We need to give it an argument or value which is a number of seconds 
# This can also be a fractional value such as 0.5 for half a second

play 60
# command = sleep
# argument = 0.5 (half a second)
sleep 0.5
play 68
